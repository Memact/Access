import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, "..")
const inputPath = process.argv[2] ? path.resolve(process.argv[2]) : path.join(repoRoot, ".data", "access-store.json")
const outputPath = process.argv[3] ? path.resolve(process.argv[3]) : path.join(repoRoot, "supabase", "seed-from-local.sql")

const raw = fs.readFileSync(inputPath, "utf8")
const store = JSON.parse(raw)
const usersById = new Map((store.users || []).map((user) => [user.id, user]))
const appResolution = buildAppResolution(store.apps || [], usersById)

const sql = []
sql.push("-- Generated by scripts/export-supabase-seed.mjs")
sql.push("-- This file replays local Access data into the Supabase-backed tables.")
sql.push("begin;")

for (const app of appResolution.canonicalApps) {
  const ownerEmail = app.owner_email
  sql.push(`
insert into public.memact_apps (
  id, owner_user_id, name, slug, description, redirect_urls, default_scopes, created_at, updated_at, revoked_at
)
select
  '${escapeSql(toUuid(app.id))}'::uuid,
  auth_user.id,
  '${escapeSql(app.name)}',
  '${escapeSql(app.slug || normalizeAppName(app.name))}',
  '${escapeSql(app.description || "")}',
  '${escapeSql(JSON.stringify(app.redirect_urls || []))}'::jsonb,
  ${toTextArray(app.default_scopes || [])},
  '${escapeSql(app.created_at)}'::timestamptz,
  '${escapeSql(app.updated_at || app.created_at)}'::timestamptz,
  ${toTimestamp(app.revoked_at)}
from auth.users as auth_user
where lower(auth_user.email) = lower('${escapeSql(ownerEmail)}')
  and not exists (
    select 1
    from public.memact_apps existing_app
    where existing_app.owner_user_id = auth_user.id
      and existing_app.slug = '${escapeSql(app.slug || normalizeAppName(app.name))}'
      and existing_app.revoked_at is null
  )
limit 1
on conflict (id) do update
set
  name = excluded.name,
  slug = excluded.slug,
  description = excluded.description,
  redirect_urls = excluded.redirect_urls,
  default_scopes = excluded.default_scopes,
  updated_at = excluded.updated_at,
  revoked_at = excluded.revoked_at;`.trim())
}

for (const key of store.api_keys || []) {
  const owner = usersById.get(key.owner_user_id)
  if (!owner?.email) continue
  const resolvedApp = appResolution.byLocalId.get(key.app_id)
  if (!resolvedApp) continue
  sql.push(`
insert into public.memact_api_keys (
  id, app_id, owner_user_id, name, key_hash, key_prefix, scopes, created_at, last_used_at, revoked_at, first_used_notified_at
)
select
  '${escapeSql(toUuid(key.id))}'::uuid,
  app.id,
  auth_user.id,
  '${escapeSql(key.name)}',
  '${escapeSql(key.key_hash)}',
  '${escapeSql(key.key_prefix)}',
  ${toTextArray(key.scopes || [])},
  '${escapeSql(key.created_at)}'::timestamptz,
  ${toTimestamp(key.last_used_at)},
  ${toTimestamp(key.revoked_at)},
  ${toTimestamp(key.first_used_notified_at)}
from auth.users as auth_user
join public.memact_apps as app
  on app.owner_user_id = auth_user.id
 and app.slug = '${escapeSql(resolvedApp.slug)}'
 and app.revoked_at is null
where lower(auth_user.email) = lower('${escapeSql(owner.email)}')
limit 1
on conflict (id) do update
set
  name = excluded.name,
  key_hash = excluded.key_hash,
  key_prefix = excluded.key_prefix,
  scopes = excluded.scopes,
  last_used_at = excluded.last_used_at,
  revoked_at = excluded.revoked_at,
  first_used_notified_at = excluded.first_used_notified_at;`.trim())
}

for (const consent of store.consents || []) {
  const owner = usersById.get(consent.user_id)
  if (!owner?.email) continue
  const resolvedApp = appResolution.byLocalId.get(consent.app_id)
  if (!resolvedApp) continue
  sql.push(`
insert into public.memact_consents (
  id, user_id, app_id, scopes, created_at, updated_at, revoked_at
)
select
  '${escapeSql(toUuid(consent.id))}'::uuid,
  auth_user.id,
  app.id,
  ${toTextArray(consent.scopes || [])},
  '${escapeSql(consent.created_at)}'::timestamptz,
  '${escapeSql(consent.updated_at || consent.created_at)}'::timestamptz,
  ${toTimestamp(consent.revoked_at)}
from auth.users as auth_user
join public.memact_apps as app
  on app.owner_user_id = auth_user.id
 and app.slug = '${escapeSql(resolvedApp.slug)}'
 and app.revoked_at is null
where lower(auth_user.email) = lower('${escapeSql(owner.email)}')
limit 1
on conflict (id) do update
set
  scopes = excluded.scopes,
  updated_at = excluded.updated_at,
  revoked_at = excluded.revoked_at;`.trim())
}

for (const entry of store.audit_log || []) {
  const owner = usersById.get(entry.user_id)
  sql.push(`
insert into public.memact_audit_log (
  id, user_id, action, details, created_at
)
select
  '${escapeSql(toUuid(entry.id))}'::uuid,
  ${owner?.email ? "auth_user.id" : "null"},
  '${escapeSql(entry.action)}',
  '${escapeSql(JSON.stringify(entry.details || {}))}'::jsonb,
  '${escapeSql(entry.created_at)}'::timestamptz
${owner?.email ? `from auth.users as auth_user
where lower(auth_user.email) = lower('${escapeSql(owner.email)}')
limit 1` : ""}
on conflict (id) do nothing;`.trim())
}

sql.push("commit;")

fs.mkdirSync(path.dirname(outputPath), { recursive: true })
fs.writeFileSync(outputPath, `${sql.join("\n\n")}\n`, "utf8")
console.log(`Wrote ${outputPath}`)

function normalizeAppName(name) {
  return String(name || "")
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
}

function escapeSql(value) {
  return String(value ?? "").replace(/'/g, "''")
}

function toTimestamp(value) {
  return value ? `'${escapeSql(value)}'::timestamptz` : "null"
}

function toTextArray(values) {
  const list = Array.isArray(values) ? values : []
  return `array[${list.map((value) => `'${escapeSql(value)}'`).join(", ")}]::text[]`
}

function toUuid(value) {
  const raw = String(value || "")
  const match = raw.match(/[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i)
  if (!match) {
    throw new Error(`Could not extract uuid from "${raw}"`)
  }
  return match[0]
}

function buildAppResolution(apps, usersById) {
  const canonicalByKey = new Map()
  const byLocalId = new Map()

  for (const app of apps) {
    const owner = usersById.get(app.owner_user_id)
    if (!owner?.email) continue
    const slug = app.slug || normalizeAppName(app.name)
    const key = `${owner.email.toLowerCase()}::${slug}`
    const current = canonicalByKey.get(key)
    if (!current || compareAppFreshness(app, current) > 0) {
      canonicalByKey.set(key, {
        ...app,
        slug,
        owner_email: owner.email
      })
    }
  }

  for (const app of apps) {
    const owner = usersById.get(app.owner_user_id)
    if (!owner?.email) continue
    const slug = app.slug || normalizeAppName(app.name)
    const key = `${owner.email.toLowerCase()}::${slug}`
    const canonical = canonicalByKey.get(key)
    if (canonical) {
      byLocalId.set(app.id, canonical)
    }
  }

  return {
    canonicalApps: [...canonicalByKey.values()],
    byLocalId
  }
}

function compareAppFreshness(left, right) {
  const leftTime = Date.parse(left.updated_at || left.created_at || 0)
  const rightTime = Date.parse(right.updated_at || right.created_at || 0)
  if (leftTime !== rightTime) return leftTime - rightTime
  return String(left.id || "").localeCompare(String(right.id || ""))
}
